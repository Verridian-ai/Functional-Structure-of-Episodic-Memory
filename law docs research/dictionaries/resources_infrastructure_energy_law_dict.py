def get_all_keywords():
    """Return all keywords across all domains."""
    all_keywords = []
    for domain_key, domain_data in RESOURCES_INFRASTRUCTURE_ENERGY_LAW["domains"].items():
        if "keywords" in domain_data:
            all_keywords.extend(domain_data["keywords"])
    return all_keywords


def get_domain_keywords(domain_name):
    """Get keywords for a specific domain."""
    domain_key = domain_name.lower().replace(" ", "_").replace("&", "").replace("-", "_")
    if domain_key in RESOURCES_INFRASTRUCTURE_ENERGY_LAW["domains"]:
        return RESOURCES_INFRASTRUCTURE_ENERGY_LAW["domains"][domain_key].get("keywords", [])
    return []


def get_all_legislation():
    """Return all legislation across all domains."""
    all_legislation = {}
    for domain_key, domain_data in RESOURCES_INFRASTRUCTURE_ENERGY_LAW["domains"].items():
        if "legislation" in domain_data:
            all_legislation[domain_key] = domain_data["legislation"]
    return all_legislation


def get_all_regulators():
    """Return all regulators across all domains."""
    all_regulators = []
    for domain_key, domain_data in RESOURCES_INFRASTRUCTURE_ENERGY_LAW["domains"].items():
        if "regulators" in domain_data:
            all_regulators.extend(domain_data["regulators"])
    return list(set(all_regulators))  # Remove duplicates


def search_keywords(search_term):
    """Search for keywords containing the search term."""
    results = []
    search_term_lower = search_term.lower()
    for domain_key, domain_data in RESOURCES_INFRASTRUCTURE_ENERGY_LAW["domains"].items():
        if "keywords" in domain_data:
            matching = [kw for kw in domain_data["keywords"] if search_term_lower in kw.lower()]
            if matching:
                results.append({
                    "domain": domain_key,
                    "matches": matching
                })
    return results


